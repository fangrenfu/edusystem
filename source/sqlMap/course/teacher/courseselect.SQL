select * from(SELECT row_number() over(ORDER BY COURSES.COURSENO) as row,COURSES.COURSENO AS kh, courses.oldcourseno jkh,COURSES.COURSENAME AS kcmc,
      COURSES.CREDITS AS xf, COURSES.HOURS AS zzxs, courses.Lhours as zjk,
      COURSES.EXPERIMENTS AS zsy, COURSES.COMPUTING AS zsj,
      COURSES.SHOURS AS zsx, COURSES.KHOURS AS ztl,
      COURSES.ZHOURS AS zzx,
      COURSETYPEOPTIONS.[VALUE] AS lb,COURSETYPEOPTIONS2.[VALUE] AS lx, RTRIM(COURSES.rem) AS bz,
      SCHOOLS.NAME AS kkdw
FROM COURSES INNER JOIN
      SCHOOLS ON COURSES.SCHOOL = SCHOOLS.SCHOOL INNER JOIN
      COURSETYPEOPTIONS ON COURSES.TYPE = COURSETYPEOPTIONS.NAME INNER JOIN
      COURSETYPEOPTIONS2 ON COURSES.TYPE2 = COURSETYPEOPTIONS2.NAME WHERE COURSES.SCHOOL LIKE (:SCHOOL) AND COURSES.COURSENO LIKE (:COURSENO) AND courses.oldcourseno like :OLDCOURSENO and COURSES.COURSENAME LIKE (:COURSENAME)
)as B where B.row between :start and :end;


