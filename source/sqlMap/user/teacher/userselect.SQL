select * FROM(select ROW_NUMBER() OVER(ORDER BY TEACHERS.TEACHERNO) as ROW,RTRIM(USERS.USERNAME) AS yhm,RTRIM(TEACHERS.TEACHERNO) AS jsh,
RTRIM(TEACHERS.NAME) AS xm,
POSITIONS.VALUE AS zc,
SCHOOLS.NAME AS xy,
SEXCODE.NAME AS xb,
RTRIM(USERS.ROLES) AS js FROM USERS JOIN TEACHERS ON USERS.TEACHERNO=TEACHERS.TEACHERNO
LEFT OUTER JOIN POSITIONS ON TEACHERS.POSITION=POSITIONS.NAME
LEFT OUTER JOIN SEXCODE ON TEACHERS.SEX=SEXCODE.CODE
LEFT OUTER JOIN SCHOOLS ON TEACHERS.SCHOOL=SCHOOLS.SCHOOL WHERE (USERS.USERNAME LIKE :USER)
AND(SCHOOLS.NAME LIKE :DANWEI)
AND(USERS.ROLES LIKE :ROLE)) as B
where B.ROW BETWEEN :start AND :end