SELECT * from
  (SELECT row_number() over(ORDER BY APPLYDATE DESC)AS ROW, 
  RECNO,ROOMNO,JSN,EQUIPMENT,STATUS,RESERVED,DAY,TIME,OEW,WEEKS,SCHOOL,CONVERT(varchar(10),APPLYDATE,20) AS APPLYDATE,USERNAME,APPROVED,PURPOSE
   FROM VIEWROOMLEASE
   WHERE YEAR= :YEAR
     AND TERM= :TERM
     AND SCHOOLCODE LIKE :SCHOOL
     AND ROOMNO LIKE :ROOMNO
     AND (APPROVED= :APPROVED
          OR APPROVED= :NOTAPPROVED) )AS b
WHERE b.ROW BETWEEN :start AND :end