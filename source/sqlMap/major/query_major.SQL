select * from(SELECT row_number() over(order by MAJORS.MAJORNO) as row,MAJORS.MAJORNO,
MAJORS.YEARS xz,
MAJORS.SCHOOL,
MAJORS.DEGREE ,
BRANCHCODE.NAME AS xk ,
MAJORCODE.NAME AS zymc,
SCHOOLS.NAME AS xy,
DEGREEOPTIONS.NAME AS xw,
MAJORS.REM AS bz,
MAJORS.ROWID
FROM MAJORS JOIN MAJORCODE ON MAJORS.MAJORNO=MAJORCODE.CODE
JOIN BRANCHCODE ON MAJORS.BRANCH=BRANCHCODE.CODE
JOIN SCHOOLS ON MAJORS.SCHOOL=SCHOOLS.SCHOOL
JOIN DEGREEOPTIONS ON MAJORS.DEGREE=DEGREEOPTIONS.CODE
WHERE MAJORS.MAJORNO LIKE :MAJORNO
AND cast(MAJORS.YEARS as char(1)) LIKE :YEARS
AND MAJORS.DEGREE LIKE :DEGREE
AND MAJORS.SCHOOL LIKE :SCHOOL
) as b where b.row between :start and :end