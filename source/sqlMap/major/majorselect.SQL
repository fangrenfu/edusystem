select * from(SELECT row_number() over(order by MAJORS.MAJORNO)as row,
MAJORS.YEARS,
RTRIM(BRANCHCODE.NAME) AS BRANCHNAME,
RTRIM(MAJORCODE.NAME) AS MAJORNAME,
RTRIM(SCHOOLS.NAME) AS SCHOOLNAME,
RTRIM(SCHOOLS.SCHOOL) AS SCHOOL,
RTRIM(DEGREEOPTIONS.NAME) AS DEGREENAME,
RTRIM(MAJORS.REM) AS REM,
MAJORS.ROWID
FROM MAJORS JOIN MAJORCODE ON MAJORS.MAJORNO=MAJORCODE.CODE
JOIN BRANCHCODE ON MAJORS.BRANCH=BRANCHCODE.CODE
JOIN SCHOOLS ON MAJORS.SCHOOL=SCHOOLS.SCHOOL
JOIN DEGREEOPTIONS ON MAJORS.DEGREE=DEGREEOPTIONS.CODE
WHERE MAJORS.MAJORNO LIKE :MAJORNO
AND cast(MAJORS.YEARS as char(2)) LIKE :YEARS
AND MAJORS.DEGREE LIKE :DEGREE
AND MAJORS.SCHOOL LIKE :SCHOOL)as b where b.row between :start and :end